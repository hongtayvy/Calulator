public class CalculatorOperations {
    public static int addLogic(int numberOne, int numberTwo){
        return (numberOne + numberTwo);
    }

    public static int subtractLogic(int numberOne, int numberTwo){
        return (numberOne - numberTwo);
    }

    public static int multiplyLogic(int numberOne, int numberTwo){
        return (numberOne * numberTwo);
    }

    public static int divideLogic(int numberOne, int numberTwo){
        return (numberOne / numberTwo);
    }
}
